/*
* Precência de operadores e associatividade
------------------------------------------------------*/

console.log(2 + 4 * 10); // 42
// ^ mesma precedência matemática

// Forçando precedência com parênteses
console.log((2 + 4) * 10); // 42

// Associativade entre multiplicação e divisão
console.log(5 * 10 / 2); // 25
console.log(10 / 2 * 5); //25

// A precedência é a mesma!
